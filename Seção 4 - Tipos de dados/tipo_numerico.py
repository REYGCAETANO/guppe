"""
Tipo númerico
"""
num = 1_000_000

print(f"Valor da variável: {num}")
print(f"Tipo de dado da variável: {type(num)}")
print(f"Adicionado valores a variável: {num.__add__(1_000_000)}")
print(f"Divisão simples: {num / 2}")
print(f"Divisão com resultado inteiro: {num // 3}")
print(f"Resto da divisão: {num % 2}")
print(f"Multplicação: {num * 2}")
print(f"Potência: {num ** 2}")
